package main

func arithmeticOperations() {
	add := 1 + 2
	println(add)

	substract := 10 - 5
	println(substract)

	remainder := 5 % 2
	println(remainder)

	divide := 10 / 5
	println(divide)

	increment := 1
	increment++
	println(increment)

	increment--
	println(increment)

	increment += 5
	println(increment)

	increment *= 2
	println(increment)
}

// Prints
// 3
// 5
// 1
// 2
// 2
// 1
// 6
// 12
